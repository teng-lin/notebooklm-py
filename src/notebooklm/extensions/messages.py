def get_entry_message(lang: str, key: str, **kwargs) -> str:
    """Get entry point message in specified language"""
    text = ENTRY_MESSAGES.get(lang, ENTRY_MESSAGES["en"]).get(key, key)
    if kwargs:
        text = text.format(**kwargs)
    return text


def get_ui_message(lang: str, key: str) -> str:
    """Get analyzer UI message in specified language"""
    return UI_MESSAGES.get(lang, UI_MESSAGES["en"]).get(key, key)
